// ── Breadcrumb / Room History ──────────────────────────────────────────────────
// Tracks which rooms (rooms) the user has visited, in order.
// Stored in sessionStorage so it persists across same-tab navigation.
export const History = (() => {
  const KEY = 'undercity_hist_${proj.id}';
  function _load() { try { return JSON.parse(sessionStorage.getItem(KEY) ?? '[]'); } catch { return []; } }
  function _save(h) { sessionStorage.setItem(KEY, JSON.stringify(h)); }
  return {
    push(roomId) { const h = _load(); if (h[h.length-1] !== roomId) { h.push(roomId); _save(h); } },
    pop()           { const h = _load(); const s = h.pop(); _save(h); return s; },
    peek()          { const h = _load(); return h[h.length-1]; },
    all()           { return _load(); },
    clear()         { _save([]); },
  };
})();

// ── User ────────────────────────────────────────────────────────────────────
// The User is the player navigating the dungeon (the person filling the form).
// In MUD terms: the user inhabits a room (location) and carries an inventory.
//
//   User.location  — current room ID (room the user is standing in)
//   User.carry()   — pick up a named item (rich object) into inventory
//   User.drop()    — discard a carried item
//   User.has()     — check whether an item is currently carried
//   User.inspect() — examine a carried item
//
// User.* also proxies all Inventory methods for backwards compatibility so
// that "user.set", "user.get" etc. continue to work in payloads.
export const User = (() => {
  return {
    /** Current room ID — read from the page's data-node attribute on <body>. */
    get location() {
      return document.body?.dataset?.node ?? History.peek() ?? null;
    },

    /**
     * Pick up a named item and carry it in inventory.
     *   User.carry('authToken', { token: 'abc', expires: '2026-12-31' })
     *   User.carry('medicalForm', { type: 'form', status: 'incomplete', data: {} })
     */
    carry(itemId, itemData = {}) {
      Inventory.set(itemId, {
        _isItem: true,
        _carriedAt: new Date().toISOString(),
        ...itemData,
      });
    },

    /** Drop (remove) a carried item from inventory. */
    drop(itemId) { Inventory.delete(itemId); },

    /** Returns true if the user is carrying a non-empty value for itemId. */
    has(itemId) {
      const v = Inventory.get(itemId);
      return v !== null && v !== undefined && v !== '' && v !== false;
    },

    /** Examine the full value of a carried item. */
    inspect(itemId) { return Inventory.get(itemId); },

    // ── Inventory delegates ────────────────────────────────────────────────────
    get(key)        { return Inventory.get(key); },
    set(key, value) { Inventory.set(key, value); },
    merge(obj)      { Inventory.merge(obj); },
    delete(key)     { Inventory.delete(key); },
    clear()         { Inventory.clear(); },
    check(expr)     { return Inventory.check(expr); },
    dump()          { return Inventory.dump(); },
  };
})();

