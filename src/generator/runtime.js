/**
 * runtime.js — Generates the runtime.js bundle embedded in every Undercity app.
 *
 * The runtime provides:
 *   Inventory  — session-persisted key/value store
 *   Bus        — typed event bus
 *   Navigator  — page-routing helpers (entry node resolves to index.html)
 *   Actions    — built-in action library (DOM, Forms, HTTP, UI)
 *   route()    — diamond routing helper
 *   runPayload() — executes a node's action payload array
 *
 * Namespace aliases in runPayload (case-insensitive, common shorthands):
 *   inventory / Inventory → Inventory
 *   navigator / Navigator → Navigator
 *   actions / Actions / dom / form / ui → Actions
 *
 * The "into" param: any action that returns a value can store it in Inventory
 * by setting { "into": "fieldName" } in the params object.
 *
 * Plugins contribute extension blocks appended after the core (registerNamespace).
 */

